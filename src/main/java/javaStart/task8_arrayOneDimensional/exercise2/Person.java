package javaStart.task8_arrayOneDimensional.exercise2;

public class Person {
    private int id;
    private int weight;

    public Person(int id, int weight) {
        this.id = id;
        this.weight = weight;
    }

    public int getId() {
        return id;
    }

    public int getWeight() {
        return weight;
    }
}
