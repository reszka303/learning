package javaStart.task3_MethodsAndConstructors.exercise4;

public class HousePrint {
    String printTakeShower() {
        return "We're having a shower";
    }

    String printTakeBath() {
        return "We're having a bath";
    }

    String printMakeDinner() {
        return "We're making dinner";
    }

    String printBoilWater() {
        return "We're boiling some water";
    }

    String printWatchTV(int hours) {
        return "We're watching TV by " + hours + " hours";
    }

    String printHeatHouse() {
        return "We're heating a house";
    }

    String printFuelWater() {
        return "We're fueling up water";
    }

    String printFuelOil() {
        return "We're fueling up oil";
    }
}
